class testautounbox
{
	public static void main(String args[])
	{
		Float F=10.5F;
		Double D=20.55D;
		Character C='c';
		Boolean B=true;
		float f= F;
		double d= D;
		char c=C;
		boolean b=B;
		
	System.out.println("unboxed float is "+f);
	System.out.println("unboxed double is "+d);
	System.out.println("unboxed char is "+c);
	System.out.println("unboxed boolean is "+b);
	}
}	